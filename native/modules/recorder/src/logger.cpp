#include "logger.h"
#include "logger_callback.h"
#include <cstdarg>
#include <cstdio>

void Logger::debug(const char* fmt, ...) {
    va_list args;
    va_start(args, fmt);
    char buffer[1024];
    vsnprintf(buffer, sizeof(buffer), fmt, args);
    va_end(args);
    
    log_debug(buffer);
}

